import React, { useState, useEffect } from 'react';
import { Link, useLocation } from 'react-router-dom';

const navigation = [
  { name: 'Dashboard', path: '/dashboard', icon: DashboardIcon },
  { name: 'Employees', path: '/employees', icon: UsersIcon },
  { name: 'Inventory', path: '/inventory', icon: ArchiveBoxIcon },
  { name: 'Sales', path: '/sales', icon: CurrencyDollarIcon },
  { name: 'Reports', path: '/reports', icon: DocumentChartBarIcon },
  { name: 'System Administration', path: '/system-admin', icon: ShieldCheckIcon },
  { name: 'Settings', path: '/settings', icon: Cog6ToothIcon },
];

export default function Sidebar({ isOpen, setIsOpen }: { isOpen: boolean; setIsOpen: (val: boolean) => void }) {
  // Mobile check
  const [isMobile, setIsMobile] = useState(false);

  useEffect(() => {
    const handleResize = () => {
      setIsMobile(window.innerWidth < 768);
      if (window.innerWidth < 768) {
        setIsOpen(false);
      } else {
        setIsOpen(true);
      }
    };
    handleResize();
    window.addEventListener('resize', handleResize);
    return () => window.removeEventListener('resize', handleResize);
  }, [setIsOpen]);

  // For the active path (mocking react-router if not wrapped)
  const location = useLocation();
  const currentPath = location.pathname;

  return (
    <>
      {/* Mobile overlay */}
      {isMobile && isOpen && (
        <div 
          className="fixed inset-0 bg-black/50 z-20 transition-opacity"
          onClick={() => setIsOpen(false)}
        />
      )}

      {/* Sidebar container */}
      <aside
        className={`fixed md:static inset-y-0 left-0 z-30 flex flex-col bg-base-200 border-r border-base-300 transition-all duration-300 ease-in-out ${
          isOpen ? 'w-64 translate-x-0' : 'w-20 -translate-x-full md:translate-x-0'
        }`}
      >
        <div className="flex items-center justify-center h-16 border-b border-base-300">
          <h1 className={`font-bold text-primary transition-all duration-300 ${isOpen ? 'text-2xl' : 'text-sm scale-0 md:scale-100 hidden md:block'}`}>
            {isOpen ? 'ERP Admin' : 'ERP'}
          </h1>
        </div>

        <nav className="flex-1 overflow-y-auto py-4">
          <ul className="menu px-2 gap-2">
            {navigation.map((item) => {
              const isActive = currentPath === item.path || (currentPath === '/' && item.path === '/dashboard');
              return (
                <li key={item.name}>
                  <Link
                    to={item.path}
                    className={`flex items-center ${isOpen ? 'justify-start' : 'justify-center'} ${
                      isActive ? 'active bg-primary text-primary-content' : 'hover:bg-base-300'
                    }`}
                    title={!isOpen ? item.name : undefined}
                  >
                    <item.icon className={`w-6 h-6 ${!isOpen ? 'mx-auto' : ''}`} />
                    {isOpen && <span className="ml-3 transition-opacity duration-300">{item.name}</span>}
                  </Link>
                </li>
              );
            })}
          </ul>
        </nav>

        {/* User profile mini badge at bottom */}
        <div className="p-4 border-t border-base-300">
          <div className={`flex items-center ${isOpen ? 'justify-start' : 'justify-center'}`}>
            <div className="avatar placeholder">
              <div className="bg-neutral text-neutral-content rounded-full w-10 flex items-center justify-center">
                <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth="1.5" stroke="currentColor" className="w-5 h-5">
                  <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 6a3.75 3.75 0 1 1-7.5 0 3.75 3.75 0 0 1 7.5 0ZM4.501 20.118a7.5 7.5 0 0 1 14.998 0A17.933 17.933 0 0 1 12 21.75c-2.676 0-5.216-.584-7.499-1.632Z" />
                </svg>
              </div>
            </div>
            {isOpen && (
              <div className="ml-3 overflow-hidden text-ellipsis whitespace-nowrap">
                <p className="font-semibold text-sm">David Simbajon</p>
                <p className="text-xs opacity-70">Administrator</p>
              </div>
            )}
          </div>
        </div>
      </aside>
    </>
  );
}

